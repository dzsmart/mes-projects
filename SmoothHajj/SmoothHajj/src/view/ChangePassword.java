    private void ApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApplyActionPerformed
        String newPass = String.valueOf(newPassword.getPassword()), Confirmation = String.valueOf(confirmNewPassword.getPassword());
        if(!newPass.equals(Confirmation)) {
            JOptionPane.showMessageDialog(null, "Le Nouveau Mot De Passe Et Son Confirmation Ne Correspondent Pas !");
            return;
        }
        
        if(newPass.length() < 4) {
            JOptionPane.showMessageDialog(null, "Le Nouveau Mot De Passe Est Trop Court !");
            return;
        }
        
        Query q = manager.createQuery("SELECT l FROM Login l WHERE l.Username = '"+Username.getText()+"' AND l.Password = '"+String.valueOf(oldPassword.getPassword())+"'");
        List<Login> Logins = q.getResultList();
        if(Logins.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nom et/ou mot de passe incorrect !");
            return;
        }
        Login l = Logins.get(0);
        try {
            manager.getTransaction().begin();
            manager.createNamedQuery("Login.UpdateLogin", Login.class)
                    .setParameter("username", Username.getText())
                    .setParameter("password", newPass)
                    .executeUpdate();
            manager.getTransaction().commit();
            JOptionPane.showMessageDialog(null, "Mot De Passe Correctement Chang√© !");
            new LoginFrame().setVisible(true);
            this.dispose();
        } catch(Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erreur Survenue Lors De Changement De Mot De Passe !");
        }
    }//GEN-LAST:event_ApplyActionPerformed

